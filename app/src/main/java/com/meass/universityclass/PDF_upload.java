package com.meass.universityclass;

public class PDF_upload {
    String name,url;

    public PDF_upload() {
    }

    public PDF_upload(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
